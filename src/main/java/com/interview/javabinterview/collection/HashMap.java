    static class Node<K, V> implements Map.Entry<K, V> {
        final int hash;
        final K key;
        V value;
        Node<K, V> next;

        Node(int hash, K key, V value, Node<K, V> next) {
            this.hash = hash;
            this.key = key;
            this.value = value;
            this.next = next;
        }

        public final K getKey() {
            return key;
        }

        public final V getValue() {
            return value;
        }

        public final String toString() {
            return key + "=" + value;
        }

        public final int hashCode() {
            return Objects.hashCode(key) ^ Objects.hashCode(value);
        }

        public final V setValue(V newValue) {
            V oldValue = value;
            value = newValue;
            return oldValue;
        }

        public final boolean equals(Object o) {
            if (o == this)
                return true;
            if (o instanceof Map.Entry) {
                Map.Entry<?, ?> e = (Map.Entry<?, ?>) o;
                if (Objects.equals(key, e.getKey()) &&
                        Objects.equals(value, e.getValue()))
                    return true;
            }
            return false;
        }
    }

    /**
     * index =  (n-1) & hash
     * index = hash % n
     * 当n 是 2的次方的时候 两个算法是相同的.
     * <p>
     * 上面两种方法都存在一种缺陷，就是取余的计算结果对高位是无效的，只是对低位有效，
     * 当计算出来的hasCode()只有高位有变化时，取余的结果还是一样的。
     * <p>
     * 例如:
     * int hashCode1 = 01110101
     * int hasdCode2 = 01010101
     * <p>
     * int index1 = 01110101 & 1111 -> 0101
     * int index2 - 01010101 & 1111 -> 0101
     * <p>
     * //十进制翻译
     * int hashCode1 = 117
     * int hashCode2 = 85
     * <p>
     * int index1 = 117 % 16  -> 5
     * int index2 = 85 % 16  -> 5
     * <p>
     * 从上面的例子可以看出来，当key计算出来的hashCode()只有高位变化时，
     * 最终算出来的index索引就会引起hash冲突，如果冲突太多的话，HashMap的效率就会非常低下了;
     * h >>> 16:
     * 解释:hashCode 是32位的,首先对hashCode进行16位的无符号右移,然后再与自身与或运算.
     * <p>
     * 假设hashCode是8位的
     * int hashCode1 = 01110101 >>> 4
     * --> hashCode1 = 00000111
     * <p>
     * int hasCode2 = 01010101 >>> 4
     * -->hasCode2 = 00000101
     * <p>
     * 对自身进行与或运算:
     * hashCode1 = 01110101 ^ 00000111
     * --> hashCode1 = 01110010
     * <p>
     * hashCode2 = 01010101 ^ 00000101
     * --> hashCode2 = 01010000
     * <p>
     * 最后，取余
     * hashCode1 = 01110010 & 1111 = 0010
     * hashCode2 = 01010000 & 1111 = 0000
     * <p>
     * 通过上面的分析，hash的再次计算能够把高位的变化影响到了低位的变化
     */
    static final int hash(Object key) {
        int h;
        return (key == null) ? 0 : (h = key.hashCode()) ^ (h >>> 16);
    }


    static Class<?> comparableClassFor(Object x) {
        if (x instanceof Comparable) {
            Class<?> c;
            Type[] ts, as;
            Type t;
            ParameterizedType p;
            if ((c = x.getClass()) == String.class) // bypass checks
                return c;
            if ((ts = c.getGenericInterfaces()) != null) {
                for (int i = 0; i < ts.length; ++i) {
                    if (((t = ts[i]) instanceof ParameterizedType) &&
                            ((p = (ParameterizedType) t).getRawType() ==
                                    Comparable.class) &&
                            (as = p.getActualTypeArguments()) != null &&
                            as.length == 1 && as[0] == c) // type arg is c
                        return c;
                }
            }
        }
        return null;
    }


    static int compareComparables(Class<?> kc, Object k, Object x) {
        return (x == null || x.getClass() != kc ? 0 :
                ((Comparable) k).compareTo(x));
    }

    /**
     * | : 或运算，第一个操作数的的第n位于第二个操作数的第n位 只要有一个是1，那么结果的第n为也为1，否则为0
     * 然后移动 1 2 4 8 16 总共32位
     */
    static final int tableSizeFor(int cap) {
        int n = cap - 1;
        n |= n >>> 1;
        n |= n >>> 2;
        n |= n >>> 4;
        n |= n >>> 8;
        n |= n >>> 16;
        return (n < 0) ? 1 : (n >= MAXIMUM_CAPACITY) ? MAXIMUM_CAPACITY : n + 1;
    }

